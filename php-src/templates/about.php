
<h2>About</h2>

<p>Given the fact that benchmarking is one of the most prevailing RIS3 methods 
    applied essentially by many regions, <i>Benchmarking</i> tool is the main 
    tool for performing more sophisticated computational comparisons between 
    regions, into a web-based dashboard. This application helps RIS3 stakeholders 
    to derive basic information regarding the place of their region compared to 
    others, in a quick manner.</p>

<p>The proposed tool provides a web interface where users can import regional 
    data and then select a region of their preference to compare it against 
    other regions. Various statistical metrics are obtained (minimum, maximum, 
    mean, quartiles, Kernel density diagrams etc.). Î¤he user can export 
    benchmarking results in the form of tables and figures, or print the final 
    benchmarking report produced by this application. This framework provides 
    the user with the ability to import his own data, by importing an excel file 
    from his local space or use regional data using <i>Regional Assets Mapping</i> tool. 
</p>

<p>The logical steps of the application, based on its methodological description are:</p>

<ul class='dashed'>
    <li><i>Step 1.</i> Data <b>import</b> tables into the framework.</li>
    <li><i>Step 2.</i> <b>Selection</b> of group of regions to examine.</li>
    <li><i>Step 3.</i> Statistical <b>analysis</b> on regional data will be performed.</li>
    <li><i>Step 4.</i> <b>Tables</b> and <b>charts</b> will be generated.</li>
    <li><i>Step 5.</i> The user can <b>save</b> and <b>export</b> the results.</li>
    <li><i>Step 6.</i> A final benchmarking <b>report</b> will be produced, for the user to be able to make an extended interpretation of the results.</li>
</ul>

<p>Following the application architecture described in previous steps, the 
    information flows within the application are given in <b>Figure 1</b>, below. 
    It should be highlighted that the initial input coming from the user, 
    defines the group of regions that will be analysed, as well as the period 
    to which it refers to. The processing of these data, in terms of benchmarking 
    analysis, is based on table or graphic format, which will be illustrated on 
    the application. Individual tables or graphics including the results, can be 
    extracted through the application.
</p>

<p>The main output of the application is the final report of Benchmarking 
    exercise, which will give the user a comprehensive picture regarding his/her 
    region compared to a group of other regions, for the chosen time frame. 
</p>

