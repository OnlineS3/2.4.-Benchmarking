    <p class='steps'>
        Step 2 - Results of the analysis
    </p> 
    <p>
        In the second step of the application, the user will be able to see the 
        selected indicators for the benchmarking exercise in a table format by 
        pressing the Refresh button <strong>(Fig. 2a-d)</strong>. The results are grouped into three 
        categories: 
    </p>
    
    <ol>
    <li>Descriptive statistics: this section includes information referring to 
        the selected group of regions regarding the min, max and average values, 
        as well as the differences between these values and the reference region.
    
